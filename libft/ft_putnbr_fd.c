#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		nbr = (long)n * -1;
	}
	else
		nbr = n;
	if (nbr > 9)
		ft_putnbr_fd(nbr / 10, fd);
	ft_putchar_fd(nbr % 10 + 48, fd);
}
